public class AsValue {
    private int x;
    private int y;

    public AsValue(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public AsValue() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
